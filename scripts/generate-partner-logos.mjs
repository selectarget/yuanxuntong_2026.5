import { promises as fs } from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const partnerDir = path.join(projectRoot, "public", "images", "PARTNER");
const outputFile = path.join(projectRoot, "src", "lib", "partner-logos.generated.ts");

const IMAGE_EXTENSIONS = new Set([".png", ".jpg", ".jpeg", ".webp", ".svg", ".gif", ".avif"]);

function encodePublicPath(fileName) {
  return `/images/PARTNER/${encodeURIComponent(fileName)}`;
}

async function main() {
  const entries = await fs.readdir(partnerDir, { withFileTypes: true });
  const logos = entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => IMAGE_EXTENSIONS.has(path.extname(name).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, "zh-Hans-CN", { numeric: true, sensitivity: "base" }))
    .map(encodePublicPath);

  const fileContent = [
    "// Auto-generated by scripts/generate-partner-logos.mjs",
    "// Do not edit manually.",
    "",
    "export const partnerLogos = [",
    ...logos.map((logo) => `  "${logo}",`),
    "] as const;",
    "",
  ].join("\n");

  await fs.writeFile(outputFile, fileContent, "utf8");
  console.log(`Generated ${logos.length} partner logos -> ${path.relative(projectRoot, outputFile)}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});

