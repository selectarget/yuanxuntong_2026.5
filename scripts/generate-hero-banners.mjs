import { promises as fs } from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const bannerDir = path.join(projectRoot, "public", "images", "banner");
const outputFile = path.join(projectRoot, "src", "lib", "hero-banners.generated.ts");

const IMAGE_EXTENSIONS = new Set([".png", ".jpg", ".jpeg", ".webp", ".svg", ".gif", ".avif"]);

function encodePublicPath(fileName) {
  return `/images/banner/${encodeURIComponent(fileName)}`;
}

async function main() {
  const entries = await fs.readdir(bannerDir, { withFileTypes: true });
  const bannerImages = entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => IMAGE_EXTENSIONS.has(path.extname(name).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, "zh-Hans-CN", { numeric: true, sensitivity: "base" }))
    .map(encodePublicPath);

  const fileContent = [
    "// Auto-generated by scripts/generate-hero-banners.mjs",
    "// Do not edit manually.",
    "",
    "export const heroBannerImages = [",
    ...bannerImages.map((image) => `  \"${image}\",`),
    "] as const;",
    "",
  ].join("\n");

  await fs.writeFile(outputFile, fileContent, "utf8");
  console.log(`Generated ${bannerImages.length} banner images -> ${path.relative(projectRoot, outputFile)}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});

